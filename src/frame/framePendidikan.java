    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
        if (txtIdPendidikan.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "ID Belum diisi");
            txtIdPendidikan.requestFocus();
        } else if (txtNamaPendidikan.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama Pendidikan Belum Diisi");
            txtNamaPendidikan.requestFocus();
        }else if (txtNip.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nip Belum Diisi");
            txtNip.requestFocus();
        }else if (txtJurusan.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Jurusan Belum Diisi");
            txtJurusan.requestFocus();
        }else if (txtTingkatPendidikan.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Tingkat Pendidikan Belum Diisi");
            txtTingkatPendidikan.requestFocus();        
        }else if (txtTahunLulus.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Tahun Lulus Belum Diisi");
            txtTahunLulus.requestFocus();
        }else {
            // ðŸ”¹ Tambahkan log agar tahu event dijalankan
            System.out.println("Tombol Tambah Ditekan");

            // ðŸ”¹ Panggil fungsi simpan (tidak ubah strukturnya)
            tblpendidikan.SimpanAnggota(
                Integer.parseInt(txtIdPendidikan.getText()),
                txtNamaPendidikan.getText(),
                Integer.parseInt(txtNip.getText()),
                txtJurusan.getText(),
                txtTingkatPendidikan.getText(),
                txtTahunLulus.getText()
            );
            tblpendidikan.tampilDataAnggota(tablePendidikan, "SELECT * FROM pendidikan");

            // ðŸ”¹ Tambahkan indikator berhasil
            if (tblpendidikan.validasi == true){
                JOptionPane.showMessageDialog(null, "ID sudah terdaftar! Data tidak disimpan.");
                txtIdPendidikan.setText(String.valueOf(tblpendidikan.VAR_idpendidikan));
                txtNamaPendidikan.setText(tblpendidikan.VAR_namapendidikan);
                txtNip.setText(String.valueOf(tblpendidikan.VAR_nip));
                txtJurusan.setText(tblpendidikan.VAR_jurusan);
                txtTingkatPendidikan.setText(tblpendidikan.VAR_tingkatpendidikan);
                txtTahunLulus.setText(tblpendidikan.VAR_tahunlulus);
            } else {
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan ke database!");
                JOptionPane.showMessageDialog(null, "Coba Cek Di PHPMyAdmin!");
            }
            }   
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "ID harus berupa angka!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
            e.printStackTrace(); // tampilkan detail di console
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

