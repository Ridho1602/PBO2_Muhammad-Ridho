    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
    try {
        if (txtIdJabatan.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "ID Jabatan Belum diisi");
            txtIdJabatan.requestFocus();
        } else if (txtNip.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "NIP Belum Diisi");
            txtNip.requestFocus();
        }else if (txtJabatan.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Jabatan Belum Diisi");
            txtJabatan.requestFocus();
        }else if (txtMasaKerjaTahun.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Masa Kerja Tahun Belum Diisi");
            txtMasaKerjaTahun.requestFocus();
        }else if (txtMasaKerjaBulan.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Masa Kerja Bulan Belum Diisi");
            txtMasaKerjaBulan.requestFocus();
        }else {
            // ðŸ”¹ Tambahkan log agar tahu event dijalankan
            System.out.println("Tombol Tambah Ditekan");

            // ðŸ”¹ Panggil fungsi simpan (tidak ubah strukturnya)
            tbljabatan.SimpanAnggota(
                Integer.parseInt(txtIdJabatan.getText()),
                Integer.parseInt(txtNip.getText()),
                txtJabatan.getText(),
                txtMasaKerjaTahun.getText(),
                txtMasaKerjaBulan.getText()
            );
            tbljabatan.tampilDataAnggota(tableJabatan, "SELECT * FROM jabatan");

            // ðŸ”¹ Tambahkan indikator berhasil
            if (tbljabatan.validasi == true){
                JOptionPane.showMessageDialog(null, "ID sudah terdaftar! Data tidak disimpan.");
                txtIdJabatan.setText(String.valueOf(tbljabatan.VAR_idjabatan));
                txtNip.setText(String.valueOf(tbljabatan.VAR_nip));
                txtJabatan.setText(tbljabatan.VAR_jabatan);
                txtMasaKerjaTahun.setText(tbljabatan.VAR_masakerjatahun);
                txtMasaKerjaBulan.setText(tbljabatan.VAR_masakerjabulan);
            } else {
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan ke database!");
            }
            }   
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "ID harus berupa angka!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
            e.printStackTrace(); // tampilkan detail di console
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

