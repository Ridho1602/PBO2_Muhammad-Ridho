    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
    try {
        if (txtIdJabatan.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan ID Jabatan yang ingin dihapus!");
            return;
        }

        int id = Integer.parseInt(txtIdJabatan.getText());
        tbljabatan.hapusAnggota01(id);

        // Kosongkan textfield setelah data berhasil dihapus
        txtIdJabatan.setText(null);
        txtNip.setText(null);
        txtJabatan.setText(null);
        txtMasaKerjaTahun.setText(null);
        txtMasaKerjaBulan.setText(null);
        JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "ID Jabatan harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnHapusActionPerformed

