    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
    try {
        if (txtNip.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan NIP yang ingin dihapus!");
            return;
        }

        int nip = Integer.parseInt(txtNip.getText());
        tblpegawai.hapusAnggota01(nip);

        // Kosongkan textfield setelah data berhasil dihapus
        txtNip.setText(null);
        txtNama.setText(null);
        txtTempatLahir.setText(null);
        txtAlamat.setText(null);
        txtStatus.setText(null);
        JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "NIP harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnHapusActionPerformed

