    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
    try {
        if (txtNip.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "NIP Belum diisi");
            txtNip.requestFocus();
        } else if (txtNama.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama Belum Diisi");
            txtNama.requestFocus();
        }else if (txtTempatLahir.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Tempat Lahir Belum Diisi");
            txtTempatLahir.requestFocus();
        }else if (txtAlamat.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Alamat Belum Diisi");
            txtAlamat.requestFocus();
        }else if (txtStatus.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Status Belum Diisi");
            txtStatus.requestFocus();
        }else {
            // ðŸ”¹ Tambahkan log agar tahu event dijalankan
            System.out.println("Tombol Tambah Ditekan");

            // ðŸ”¹ Panggil fungsi simpan (tidak ubah strukturnya)
            tblpegawai.SimpanAnggota(
                Integer.parseInt(txtNip.getText()),
                txtNama.getText(),
                txtTempatLahir.getText(),
                txtAlamat.getText(),
                txtStatus.getText()
            );
            tblpegawai.tampilDataAnggota(tablePegawai, "SELECT * FROM pegawai");

            // ðŸ”¹ Tambahkan indikator berhasil
            if (tblpegawai.validasi == true){
                JOptionPane.showMessageDialog(null, "NIP sudah terdaftar! Data tidak disimpan.");
                txtNip.setText(String.valueOf(tblpegawai.VAR_nip));
                txtNama.setText(tblpegawai.VAR_nama);
                txtTempatLahir.setText(tblpegawai.VAR_tempatlahir);
                txtAlamat.setText(tblpegawai.VAR_alamat);
                txtStatus.setText(tblpegawai.VAR_status);
            } else {
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan ke database!");
            }
            }   
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "NIP harus berupa angka!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
            e.printStackTrace(); // tampilkan detail di console
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

