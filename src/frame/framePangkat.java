    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:        
    try {
        if (txtNip.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "NIP Belum diisi");
            txtNip.requestFocus();
        } else if (txtPangkat.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Pangkat Belum Diisi");
            txtPangkat.requestFocus();
        }else if (txtIdPangkat.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "ID Pangkat Belum Diisi");
            txtIdPangkat.requestFocus();
        }else {
            // ðŸ”¹ Tambahkan log agar tahu event dijalankan
            System.out.println("Tombol Tambah Ditekan");

            // ðŸ”¹ Panggil fungsi simpan (tidak ubah strukturnya)
            tblpangkat.SimpanAnggota(
                Integer.parseInt(txtIdPangkat.getText()),
                Integer.parseInt(txtNip.getText()),
                txtPangkat.getText()
            );
            tblpangkat.tampilDataAnggota(tablePangkat, "SELECT * FROM pangkat");

            // ðŸ”¹ Tambahkan indikator berhasil
            if (tblpangkat.validasi == true){
                JOptionPane.showMessageDialog(null, "ID sudah terdaftar! Data tidak disimpan.");
                txtNip.setText(String.valueOf(tblpangkat.VAR_nip));
                txtIdPangkat.setText(String.valueOf(tblpangkat.VAR_idpangkat));
                txtPangkat.setText(tblpangkat.VAR_pangkat);
            } else {
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan ke database!");
            }
            }   
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "NIP harus berupa angka!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
            e.printStackTrace(); // tampilkan detail di console
        }
                   

    }//GEN-LAST:event_btnSimpanActionPerformed

